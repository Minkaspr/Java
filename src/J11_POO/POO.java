package J11_POO;

public class POO {
    /**
     * 
     * Paradigma
     *  Es todo aquel modelo, patrón o ejemplo que debe seguirse en determinada situación.
     *  En un sentido amplio, se refiere a una teoria o conjunto de teorias que sirve
     *  de modelo a seguir para resolver problemas o situaciones determinadas que se
     *  planteen.
     * 
     * Paradigma Orientado a Objetos (POO)
     *  Es la implementación de un Paradigma de Programacion.
     *  La POO se basa en la idea de un mundo lleno de objetos y que la resolucion de
     *  problemas se realiza mediante el modelado/abstracción de objetos de la vida real.
     *  Los objetos pueden comunicarse entre si mediante mensajes.
     * 
     * 
     *  En resumen:
     *  Se basa en el concepto de agrupar código y datos juntos dentro de una misma
     *  unidad llamada clase.
     *  Sus principales caracteristicas son:
     *      Abstraccion
     *      Encapsulamiento
     *      Polimorfismo
     *      Herencia
     *      UML
     */
}
